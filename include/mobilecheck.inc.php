function checkmobileWURFL(){
    $wurlf_devices =& xoops_getmodulehandler('devices', 'wurfl');
    $wurfl_device = $wurlf_devices->testUserAgent($_SERVER['HTTP_USER_AGENT'], true);
    if(is_array($wurfl_device)){
        return true;
    }else{
        return false;
    }
}

